#include <bits/stdc++.h>
using namespace std;

int main() {
	// T = number of test cases
	int T;
	cin >> T;

	while (T--) {
	    // X = number of plates
	    // Y = cost per plate
	    int X, Y;
	    cin >> X;
	    cin >> Y;

	    // Total cost
	    cout << X * Y << endl;
	}

	return 0;
}
