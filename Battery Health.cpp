#include <bits/stdc++.h>
using namespace std;

int main() {
	// T = number of test cases
	int T;
	cin >> T;

	while (T--) {
	    // M = number of rows
	    // N = number of columns
	    int M, N;
	    cin >> M >> N;

	    // Total number of words
	    cout << M * N << endl;
	}

	return 0;
}
